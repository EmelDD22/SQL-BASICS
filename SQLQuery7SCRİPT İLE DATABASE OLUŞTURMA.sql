CREATE TABLE Users(
ID INT IDENTITY(1,1),
USERNAME_ VARCHAR(50),
PASSWORD_ VARCHAR(50),
NAMESURNAME VARCHAR(100),
EMAIL VARCHAR(100),
GENDER VARCHAR(1),
BIRTHDATE DATE,
CREATEDDATE DATETIME,
TELNO1 VARCHAR(15),
TELNO2 VARCHAR(15)
CONSTRAINT PK_USERS  PRIMARY KEY CLUSTERED(ID ASC))

